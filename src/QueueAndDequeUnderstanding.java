import java.util.ArrayDeque;
import java.util.Deque;

public class QueueAndDequeUnderstanding {
    public static void main(String[] args) {
        Deque<Integer> deque = new ArrayDeque<>();
        deque.add(28);
        deque.add(19);
        deque.addFirst(1);
        deque.addLast(99);
        System.out.println(deque);















    }
}
